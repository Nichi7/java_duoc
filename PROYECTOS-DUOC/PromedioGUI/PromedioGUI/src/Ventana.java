    private boolean esNota(float nota){
        return nota>=1.0 && nota<=7.0;
    }
    
    private void verifica(javax.swing.JTextField t, char tecla){
        String teclado = t.getText();
        if(teclado.length()>0){
            teclado = teclado.substring(0,teclado.length()-1);
            if(!Character.isDigit(tecla) && tecla!='.')
                t.setText(teclado);
            if(tecla=='.' && teclado.contains("."))
                t.setText(teclado);
            }
        }
    
