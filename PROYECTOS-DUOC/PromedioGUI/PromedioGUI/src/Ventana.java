    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if(txtn1.getText().isEmpty() || txtn2.getText().isEmpty() || txtn3.getText().isEmpty()){
            JOptionPane.showMessageDialog(
                    this,
                    "Faltan Notas por Ingresar, motherfucker");
            return;
        }
        if(txtpor1.getText().isEmpty() || txtpor2.getText().isEmpty() || txtpor3.getText().isEmpty()){
            JOptionPane.showMessageDialog(
                    this,
                    "Faltan Porcentajes por Ingresar, motherfucker");
            return;
        }
        float n1,n2,n3,x1,x2,x3;
        n1 = Float.parseFloat(txtn1.getText());
        n2 = Float.parseFloat(txtn2.getText());
        n3 = Float.parseFloat(txtn3.getText());
        
        x1 = Float.parseFloat(txtpor1.getText());
        x2 = Float.parseFloat(txtpor2.getText());
        x3 = Float.parseFloat(txtpor3.getText());
        if(!esNota(n1) || !esNota(n2) || !esNota(n3)){
            JOptionPane.showMessageDialog(
                        this,
                        "Ingresa Notas Válidas, motherfucker");
                return;
        }
        if((x1+x2+x3)!=100.0){
            JOptionPane.showMessageDialog(
                        this,
                        "Cuadra tus porcentajes, motherfucker");
                return;
        }
        float calc;
        calc = n1*x1+n2*x2+n3*x3;
        calc = calc/100;
        if(calc<4.0){
            lblnota.setText("Te echaste el ramo con un "+calc+" #malacue");
            lblnota.setForeground(Color.red);
        }
        else{
            lblnota.setText("Aprobaste con un "+calc);
            lblnota.setForeground(Color.blue);
        }
    }//GEN-LAST:event_jButton1MouseClicked

