    private void verifica(javax.swing.JTextField t) {
        String teclado = t.getText();
        if (teclado.length() > 0) {
            char tecla = teclado.charAt(teclado.length() - 1);
            teclado = teclado.substring(0, teclado.length() - 1);
            if (!Character.isDigit(tecla)) {
                t.setText(teclado);
            }
        }

    }

    private void vaciar() {
        txtnombre1.setText("");
        txtcodigo.setText("");
        txtprecio.setText("");
        rbtflor.setSelected(false);
        rbtarbol.setSelected(false);
        buttonGroup1.clearSelection();

    }
