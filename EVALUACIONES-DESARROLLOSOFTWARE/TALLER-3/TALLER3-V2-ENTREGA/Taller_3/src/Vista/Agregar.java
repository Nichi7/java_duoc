    private void btningresarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btningresarMouseClicked
        if (txtnombre1.getText().isEmpty()
                || txtcodigo.getText().isEmpty()
                || txtprecio.getText().isEmpty()
                || (!rbtflor.isSelected() && !rbtarbol.isSelected())) {
            JOptionPane.showMessageDialog(this, "Faltan Datos por Ingresar");
            return;
        }

        Semilla s = new Semilla();
        s.setCodigo(Integer.parseInt(txtcodigo.getText()));
        s.setNombre(txtnombre1.getText());
        s.setPrecio(Integer.parseInt(txtprecio.getText()));
        if (rbtflor.isSelected()) {
            s.setTipo("F");
        }
        if (rbtarbol.isSelected()) {
            s.setTipo("A");
        }

        RegistroSemillas g = new RegistroSemillas();
        JOptionPane.showMessageDialog(this, g.agregar(s));
        vaciar();

// TODO add your handling code here:
    }//GEN-LAST:event_btningresarMouseClicked

