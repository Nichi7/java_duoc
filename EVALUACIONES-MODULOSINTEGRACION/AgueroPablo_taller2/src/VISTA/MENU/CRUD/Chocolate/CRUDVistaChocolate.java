    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        //Agregar
        int codigo = Integer.parseInt(jTextField1.getText());
        String nombre = jTextField2.getText();
        String descripcion = jTextField3.getText();
        int precioventa = Integer.parseInt(jTextField4.getText());
        int cantidad = Integer.parseInt(jTextField5.getText());
        String tipocolor = jTextField6.getText();
        String tiposabor = jTextField7.getText();
        String marca = jTextField8.getText();
        Boolean azucar = false;
        if (jCheckBox1.isSelected())
        {
            azucar = true;
        }

        Chocolate ch = new Chocolate();
        ch.setCodigo(codigo);
        ch.setNombre(nombre);
        ch.setDescripcion(descripcion);
        ch.setPrecioventa(precioventa);
        ch.setCantidad(cantidad);
        ch.setTipocolor(tipocolor);
        ch.setTiposabor(tiposabor);
        ch.setMarca(marca);
        ch.setAzucar(true);
        
        try {
            cn.agregarChocolate(ch);
        } catch (Exception ex) {
            //Logger.getLogger(CRUDVistaChocolate.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        limpiar();
        
    }//GEN-LAST:event_jButton4ActionPerformed

