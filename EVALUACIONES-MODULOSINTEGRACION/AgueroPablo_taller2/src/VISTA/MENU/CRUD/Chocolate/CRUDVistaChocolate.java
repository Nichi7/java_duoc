    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        //update
        int elejido = jTable1.getSelectedRow();
        if (elejido!=-1)
        {
            ModelJTableChocolate m = (ModelJTableChocolate) jTable1.getModel();
            ArrayList al = m.getRows();
            Chocolate ch = (Chocolate)al.get(elejido);
            
            String nombreEditado = (String)m.getValueAt(elejido, 1);
            String descripcionEditado = (String)m.getValueAt(elejido, 2);
            Integer precioventaEditado = (Integer)m.getValueAt(elejido, 3);
            Integer cantidadEditado = (Integer)m.getValueAt(elejido, 4);
            String tipocolorEditado = (String)m.getValueAt(elejido, 5);
            String tiposaborEditado = (String)m.getValueAt(elejido, 6);
            String marcaEditado = (String)m.getValueAt(elejido, 7);
            Boolean azucarEditado = false;
            if ((Boolean)m.getValueAt(elejido, 8))
            {
                azucarEditado = true;
            }
            
            Chocolate chocolateActualizado = new Chocolate();
            chocolateActualizado.setCodigo(ch.getCodigo());
            chocolateActualizado.setNombre(nombreEditado);
            chocolateActualizado.setDescripcion(descripcionEditado);
            chocolateActualizado.setPrecioventa(precioventaEditado);
            chocolateActualizado.setCantidad(precioventaEditado);
            chocolateActualizado.setTipocolor(tipocolorEditado);
            chocolateActualizado.setTiposabor(tiposaborEditado);
            chocolateActualizado.setMarca(marcaEditado);
            chocolateActualizado.setAzucar(azucarEditado);
                        
            try {
               cn.actualizarChocolate(chocolateActualizado);
            } catch (Exception e) {
            }
            
        }
    }//GEN-LAST:event_jButton7ActionPerformed

